    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String razaoSocial = nomeField.getText();
        String telefone = telefoneField.getText();
        String cnpj = cnpjField.getText();
        String email = emailField.getText();
        
        
        
        LogicaCadastroFornecedor logica = new LogicaCadastroFornecedor();
        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setRazaoSocial(razaoSocial);
        fornecedor.setTelefone(telefone);
        fornecedor.setCnpj(cnpj);
        fornecedor.setEmail(email);
       
        if (logica.Inserir(fornecedor)) {
                JOptionPane.showMessageDialog(this, "Cadastro Efetuado");
               
            } else {
                JOptionPane.showMessageDialog(this, "Falha no cadastro");
            }        // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

